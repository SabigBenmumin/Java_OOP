public class ex_1 {
    public static void main(String[] args) {
        // relational operators
        int heigher,lower;
        heigher = 10;
        lower = 2;
        System.out.println(heigher > lower); // more than
        System.out.println(heigher < lower); // less than
        System.out.println(heigher >= heigher); // more than or equal to
        System.out.println(heigher <= lower); // less than or equal to
        System.out.println(heigher == lower); // equal
        System.out.println(heigher != lower); // none equal
        System.out.println(heigher << 1); // << will slide bit to right
    }
}